export const COURSE_BY_ID = COURSES.reduce((acc, course) => {
    acc[course.id] = course;
    return acc;
}, {});

export function getCourseById(courseId) {
    return COURSE_BY_ID[courseId] || null;
}

export function getStarName(courseId, starIndex) {
    const course = getCourseById(courseId);
    if (!course) return "";
    const idx = Number(starIndex) - 1;
    return course.stars[idx] || "";
}
